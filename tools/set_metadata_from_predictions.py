"""Example: Using prediction screener output to set database metadata.

This demonstrates how to use the predictions CSV generated by the screener
to populate strategy attribution metadata when positions are opened.
"""
import sys
from pathlib import Path
import pandas as pd

# Add parent directory to path for imports
sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from database.strategy_metadata import bulk_set_strategy_metadata


def load_latest_predictions(reports_dir: Path = None) -> pd.DataFrame:
    """Load the most recent predictions CSV."""
    if reports_dir is None:
        repo_root = Path(__file__).resolve().parents[1]
        reports_dir = repo_root / "reports" / "screener_results"
    
    # Find latest predictions file
    pred_files = sorted(reports_dir.glob("predictions_*.csv"))
    if not pred_files:
        raise FileNotFoundError(f"No predictions CSV found in {reports_dir}")
    
    latest_file = pred_files[-1]
    print(f"Loading predictions from: {latest_file}")
    
    return pd.read_csv(latest_file)


def set_metadata_from_predictions(
    predictions_df: pd.DataFrame,
    account: str = "paper",
    top_n: int = None
) -> int:
    """Set database metadata for positions based on predictions CSV.
    
    Args:
        predictions_df: DataFrame from predictions CSV (must have columns:
            symbol, screener_rank, pred_ret, model_type, model_r2_score, etc.)
        account: 'paper' or 'live'
        top_n: Only process top N predictions (default: all)
    
    Returns:
        Number of positions updated
    
    Example usage:
        # After running prediction screener and opening positions
        predictions = load_latest_predictions()
        count = set_metadata_from_predictions(predictions, account='paper', top_n=3)
        print(f"Updated metadata for {count} positions")
    """
    if top_n:
        predictions_df = predictions_df.head(top_n)
    
    # Build metadata list for bulk update
    positions_metadata = []
    
    for _, row in predictions_df.iterrows():
        metadata = {
            'symbol': row['symbol'],
            'account': account,
            'strategy_source': row.get('strategy_source', 'predictive_model'),
            'predicted_return': row.get('pred_ret'),
            'screener_rank': row.get('screener_rank'),
        }
        
        # Add model-specific fields if present
        if 'model_type' in row and pd.notna(row['model_type']):
            metadata['model_type'] = row['model_type']
        
        # Add entry notes with model metrics
        notes_parts = []
        if 'model_r2_score' in row and pd.notna(row['model_r2_score']):
            notes_parts.append(f"R²={row['model_r2_score']:.3f}")
        if 'model_spearman_ic' in row and pd.notna(row['model_spearman_ic']):
            notes_parts.append(f"IC={row['model_spearman_ic']:.3f}")
        if 'sector' in row and pd.notna(row['sector']):
            notes_parts.append(f"Sector: {row['sector']}")
        
        if notes_parts:
            metadata['entry_notes'] = ", ".join(notes_parts)
        
        positions_metadata.append(metadata)
    
    # Bulk update
    count = bulk_set_strategy_metadata(positions_metadata)
    return count


def example_workflow():
    """Example workflow: Load predictions and update metadata."""
    print("=" * 60)
    print("Example: Setting metadata from predictions CSV")
    print("=" * 60)
    
    # Step 1: Load predictions
    try:
        predictions = load_latest_predictions()
        print(f"\nLoaded {len(predictions)} predictions")
        
        # Check if new metadata columns exist
        has_metadata = 'screener_rank' in predictions.columns and 'model_type' in predictions.columns
        
        if has_metadata:
            print(f"\n✓ CSV includes strategy attribution metadata")
            print(f"\nTop 5 predictions:")
            display_cols = ['symbol', 'screener_rank', 'pred_ret', 'model_type']
            print(predictions[display_cols].head())
        else:
            print(f"\n⚠ CSV missing metadata columns (screener_rank, model_type)")
            print("This is an older predictions file. Run the screener again to generate")
            print("a new CSV with strategy attribution metadata.")
            print(f"\nTop 5 predictions:")
            display_cols = ['symbol', 'pred_ret']
            if 'avg_ret' in predictions.columns:
                display_cols.append('avg_ret')
            print(predictions[display_cols].head())
            print("\n✗ Cannot set metadata from this CSV. Exiting.")
            return
    except FileNotFoundError as e:
        print(f"\n{e}")
        print("\nTo generate predictions, run:")
        print("  python -m src.runners.predict_screener")
        return
    
    # Step 2: Simulate setting metadata (will only update if positions exist)
    print("\n" + "=" * 60)
    print("Setting metadata for top 3 predictions...")
    print("=" * 60)
    
    count = set_metadata_from_predictions(
        predictions,
        account='paper',
        top_n=3
    )
    
    print(f"\n✓ Updated {count} position(s)")
    
    if count == 0:
        print("\nNote: No positions updated because they don't exist in the database.")
        print("This is expected if you haven't opened these positions yet.")
        print("\nWorkflow:")
        print("  1. Run prediction screener → generates CSV")
        print("  2. Open positions via Alpaca → creates positions")
        print("  3. Sync database → adds positions to DB")
        print("  4. Run this script → adds metadata from CSV")


if __name__ == "__main__":
    example_workflow()
